class Pattern4
{
	public static void main(String args[])
	{
		int i,j;
		for(i=1;i<=9;i++)
		{
			for(j=1;j<=9;j++)
			{
				if(i==j || i+j==10)
				{
					System.out.print("*");
				}
				else
				{
					System.out.print(" ");
				}
			}	System.out.println( );
		}
	System.out.println("Prabhat  Mishra");
	}
}