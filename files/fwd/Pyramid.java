class Pyramid
{
          public static void main(String[] args)
              {
                     int i,j,k=5;
                     for(i=1;i<=5;i++)
                          {
                                 for(j=1;j<k;j++)
                                 System.out.print(" ");
                                 k--;
                                 for(j=1;j<=2*i-1;j++)
                                System.out.print("*");
                                System.out.print("\n");
                           }      
                    System.out.println("\t-By Prabhat Mishra");
           } 
 }
